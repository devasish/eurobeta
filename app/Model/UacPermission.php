    public function getPermissions($role = 0) {
        $results = $this->find('all', array(
                    'conditions' => array(
                        'UacPermission.role' => $role,
                        'UacPermission.permitted' => 1,
                    ),
                    'recursive' => 1
        ));
        
        $permissions = array();
        foreach($results as $key => $result) {
            $permissions[$result['UacModule']['controller']][$result['UacModule']['action']] = $result['UacPermission']['permitted'];
        }
        
        return $permissions;
    }

